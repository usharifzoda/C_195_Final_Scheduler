package Classes;

/**
 *
 * @author DG sahib
 */
public class consultantReport 
{
    String name;
    String start;
    String end;
    String type;

    public consultantReport(String name, String start, String end, String type) {
        this.name = name;
        this.start = start;
        this.end = end;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
    
}
